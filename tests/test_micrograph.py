import itertools
import pytest
import logging
import numpy as np

from aspire.image import Image
from aspire.source import MicrographSource, Simulation

logger = logging.getLogger(__name__)
IMG_SIZES = [32, 33]
DTYPES = [np.float32, np.float64]
PARTICLES_PER_MICROGRAPHS = [5, 7, 10]
MICROGRAPH_COUNTS = [1, 2, 3]
MICROGRAPH_SIZES = [200, 500]
SIM_PARTICLES = [1, 2]
BOUNDARIES = [-1, 0, 1]

@pytest.fixture(params=itertools.product(SIM_PARTICLES, IMG_SIZES, DTYPES))
def sim_fixture(request):
    sim_particles, img_size, dtype = request.param
    simulation = Simulation(C = sim_particles, L = img_size, dtype = dtype)
    return simulation


@pytest.fixture(params=itertools.product(PARTICLES_PER_MICROGRAPHS, MICROGRAPH_COUNTS, MICROGRAPH_SIZES, BOUNDARIES))
def micrograph_fixture(sim_fixture, request):
    """
    Construct a MicrographSource
    """
    particles_per_micrograph, micrograph_count,micrograph_size,boundary = request.param
    return  MicrographSource(simulation = sim_fixture, interparticle_distance = 0, particles_per_micrograph = particles_per_micrograph, micrograph_count = micrograph_count, micrograph_size = micrograph_size, boundary = boundary)


# Test the MicrographSource class has the same values from Simulation class
def test_micrograph_source_has_simulation_values(sim_fixture, micrograph_fixture):
    s = sim_fixture
    m = micrograph_fixture
    assert s.L == m.particle_box_size
    assert s == m.simulation
    assert m.particles_per_micrograph * m.micrograph_count <= s.n

# Test the MicrographSource class raises errors when the image size is larger than micrograph size
def test_micrograph_raises_error_image_size(sim_fixture):
    with pytest.raises(Exception) as e_info:
        s = sim_fixture
        mic = MicrographSource(s, micrograph_size=s.L-1, particles_per_micrograph=10, interparticle_distance = 0)
    assert str(e_info.value) == "The micrograph size must be larger or equal to the simulation's image size."

